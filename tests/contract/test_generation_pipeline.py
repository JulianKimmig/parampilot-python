def test_every_generated_pydantic_model_rebuilds_against_public_names() -> None:
    """The complete generated graph must resolve without private runtime types."""
    from pydantic import BaseModel

    from parampilot.generated import models

    namespace = vars(models)
    model_classes = {
        value
        for value in namespace.values()
        if isinstance(value, type)
        and issubclass(value, BaseModel)
        and value is not BaseModel
    }

    assert len(model_classes) >= 490
    for model_class in model_classes:
        model_class.model_rebuild(force=True, _types_namespace=namespace)
