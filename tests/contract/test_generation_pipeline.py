def test_drift_comparison_reports_changed_and_missing_generated_files(
    tmp_path: Path,
) -> None:
    """The comparison primitive must identify both stale and absent artifacts.

    Args:
        tmp_path: Pytest-managed scratch directory.

    """
    from parampilot_codegen.artifacts import artifact_differences

    expected_root = tmp_path / "expected"
    actual_root = tmp_path / "actual"
    expected_root.mkdir()
    actual_root.mkdir()
    (expected_root / "changed.txt").write_text("expected\n")
    (actual_root / "changed.txt").write_text("stale\n")
    (expected_root / "missing.txt").write_text("required\n")

    differences = artifact_differences(
        expected_root,
        actual_root,
        (Path("changed.txt"), Path("missing.txt")),
    )

    assert differences == (
        "changed generated artifact: changed.txt",
        "missing generated artifact: missing.txt",
    )


def test_codegen_normalization_moves_union_constraints_without_mutating_input() -> None:
    """The adapter must prevent duplicate RootModel constraints losslessly."""
    from parampilot_codegen.normalization import normalize_schema_for_codegen

    source = {
        "properties": {
            "bounds": {
                "anyOf": [
                    {"items": {"type": "number"}, "type": "array"},
                    {"type": "null"},
                ],
                "maxLength": 2,
                "minLength": 2,
            }
        }
    }

    normalized = normalize_schema_for_codegen(source)

    original_bounds = source["properties"]["bounds"]
    normalized_bounds = normalized["properties"]["bounds"]
    assert original_bounds["minLength"] == 2
    assert "minLength" not in normalized_bounds
    assert "maxLength" not in normalized_bounds
    assert normalized_bounds["anyOf"][0]["minLength"] == 2
    assert normalized_bounds["anyOf"][0]["maxLength"] == 2
    assert normalized_bounds["anyOf"][1] == {"type": "null"}


def test_generated_files_have_sanitized_provenance_and_public_imports() -> None:
    """Generated source must contain no private path, import, secret, or remote ref."""
    models_path = GENERATED_ROOT / "models.py"
    source = models_path.read_text()
    schema_text = CANONICAL_SCHEMA.read_text()
    forbidden_text = (
        "/home/",
        "C:\\",
        "parampilot_backend",
        "parampilot_worker",
        "Authorization:",
        "git+ssh",
    )

    assert source.startswith("# @generated by datamodel-codegen:")
    assert "#   filename:  programmatic-openapi.json" in source.splitlines()[:3]
    assert not [item for item in forbidden_text if item in source]
    assert not [item for item in forbidden_text if item in schema_text]
    schema = json.loads(schema_text)
    remote_references: list[str] = []
    pending: list[Any] = [schema]
    while pending:
        value = pending.pop()
        if isinstance(value, dict):
            reference = value.get("$ref")
            if isinstance(reference, str) and reference.startswith(
                ("http://", "https://")
            ):
                remote_references.append(reference)
            pending.extend(value.values())
        elif isinstance(value, list):
            pending.extend(value)
    assert remote_references == []

    tree = ast.parse(source, filename=str(models_path))
    import_roots: set[str] = set()
    for node in ast.walk(tree):
        if isinstance(node, ast.Import):
            import_roots.update(alias.name.split(".")[0] for alias in node.names)
        elif isinstance(node, ast.ImportFrom) and node.module:
            import_roots.add(node.module.split(".")[0])
    assert import_roots <= {
        "__future__",
        "collections",
        "datetime",
        "enum",
        "typing",
        "uuid",
        "pydantic",
    }


