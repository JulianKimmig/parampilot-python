import pytest

PACKAGE_ROOT = Path(__file__).resolve().parents[2]
CANONICAL_SCHEMA = PACKAGE_ROOT / "contracts" / "programmatic-openapi.json"
GENERATED_ROOT = PACKAGE_ROOT / "src" / "parampilot" / "generated"
PROVENANCE_PATH = GENERATED_ROOT / "provenance.json"
UPSTREAM_SCHEMA = (
    PACKAGE_ROOT.parent / "parampilot-backend" / "schemas" / "programmatic-openapi.json"
)


def _load_json(path: Path) -> dict[str, Any]:
    """Load a JSON object from ``path``.

    Args:
        path: JSON file to decode.

    Returns:
        Decoded top-level JSON object.

    """
    value = json.loads(path.read_text())
    assert isinstance(value, dict)
    return value


def _sha256(path: Path) -> str:
    """Return the lowercase SHA-256 digest for one file.

    Args:
        path: File whose exact bytes are hashed.

    Returns:
        Hexadecimal SHA-256 digest.

    """
    return hashlib.sha256(path.read_bytes()).hexdigest()


def test_public_schema_copy_matches_private_handoff_when_available() -> None:
    """The monorepo copy must exactly match the authoritative backend artifact."""
    if not UPSTREAM_SCHEMA.is_file():
        pytest.skip("private backend source is intentionally absent")

    assert CANONICAL_SCHEMA.read_bytes() == UPSTREAM_SCHEMA.read_bytes()


def test_provenance_hashes_every_contract_input_and_generated_output() -> None:
    """The sanitized manifest must make every generated byte set auditable."""
    provenance = _load_json(PROVENANCE_PATH)
    input_metadata = provenance["input"]
    outputs = provenance["outputs"]

    assert provenance["format_version"] == 1
    assert provenance["generator"] == {
        "distribution": "datamodel-code-generator",
        "options": {
            "formatters": ["builtin"],
            "openapi_scopes": ["schemas"],
            "remote_refs": False,
            "root_model_sequence_interface": True,
            "schema_adapter": "union-constraints-v1",
            "schema_version": "3.1",
            "schema_version_mode": "strict",
        },
        "preset": "practical-py310-20260619",
        "target_pydantic": "2.12",
        "target_python": "3.10",
        "version": "0.68.1",
    }
    assert input_metadata["path"] == "contracts/programmatic-openapi.json"
    assert input_metadata["sha256"] == _sha256(CANONICAL_SCHEMA)
    assert input_metadata["bytes"] == CANONICAL_SCHEMA.stat().st_size
    assert input_metadata["paths"] == 34
    assert input_metadata["operations"] == 39
    assert input_metadata["schemas"] == 228

    from parampilot_codegen.artifacts import render_json
    from parampilot_codegen.normalization import normalize_schema_for_codegen

    normalized = render_json(normalize_schema_for_codegen(_load_json(CANONICAL_SCHEMA)))
    assert input_metadata["normalized_sha256"] == hashlib.sha256(normalized).hexdigest()
    assert input_metadata["normalized_bytes"] == len(normalized)

    for metadata in outputs.values():
        output_path = PACKAGE_ROOT / metadata["path"]
        assert output_path.is_file()
        assert metadata["sha256"] == _sha256(output_path)
        assert metadata["bytes"] == output_path.stat().st_size


